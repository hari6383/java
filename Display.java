import java.util.Scanner;
public class Display {

	public static void main(String[] args) { 
		Scanner obj=new Scanner(System.in);
		int a=obj.nextInt();
		int count=1; 
		for(int i=1;i<a;i++,System.out.println())
		{
			for(int j=1;j<=i;j++)
			{
				System.out.print(count +" ");
				count++;
			}
			
		}
	}

}