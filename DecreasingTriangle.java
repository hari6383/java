package Star_patterns;

public class DecreasingTriangle {
    public static void main(String args[])
    {
        int n=5,i,j;
        for(i=1;i<=n;i++)
        {
            for(j=i;j<=n;j++)

            {
                System.out.print("* ");
            }
            System.out.println();
        }
        
    }
}
